var num1 = parseInt(prompt("Ingresar número 1: "));
var num2 = parseInt(prompt("Ingresar número 2: "));
var num3 = parseInt(prompt("Ingresar número 3: "));
var mayor = Math.max(num1, num2, num3);
alert(`El mayor de ${num1}, ${num2} y ${num3} es ${mayor}.`);
